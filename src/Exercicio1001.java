import java.util.Scanner;

public class Exercicio1001 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		//Entrando dois valores inteiros
		int A = sc.nextInt();
		int B = sc.nextInt();	
		
		//soma A E B
		int X = A + B;
		
		//Saindo valores de x
		System.out.println("X = "+ X);
		
		sc.close();

	}

}
